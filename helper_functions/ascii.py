def print_ascii(time):
    if time == 'new_week':
        ascii_art = new_week_ascii_art()
    elif time == 'weekend':
        ascii_art = weekend_ascii_art()
    elif time == 'game over':
        ascii_art = game_over()
    else:
        ascii_art = congratulations()
    for line in ascii_art:
        print(line)


